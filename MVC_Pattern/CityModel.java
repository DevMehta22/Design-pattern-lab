package MVC_Pattern;

public class CityModel {
    private String  name, country;

    public String getName(){
        return name;
    }

    public void setName(String  name){
        this.name = name;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }
}
